# Page config
# --------------------------------------------------
st.set_page_config(
    page_title="Blackjack AI Assistant",
    page_icon="🃏",
    layout="centered",
)

st.title("🃏 Blackjack AI Assistant")
st.write(
    "Get a real-time blackjack recommendation for a two-deck game, "
    "plus an explanation generated by a rules engine and an optional nanoGPT model."
)


