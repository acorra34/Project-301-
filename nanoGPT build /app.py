import streamlit as st

try:
    import torch
    from model import GPT, GPTConfig
    TORCH_AVAILABLE = True
except Exception:
    torch = None
    GPT = None
    GPTConfig = None
    TORCH_AVAILABLE = False

from blackjack_engine import recommend_action, format_for_gpt



